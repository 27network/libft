#include <ft/math.h>

uint64_t	ft_sqrt(uint64_t n)
{
	uint64_t	i;
	uint64_t	target;

	if (n <= 1)
		return (n);
	i = 0;
	target = ft_min(n, 46342);
	while ((i * i) < target)
		i++;
	return (i);
}
