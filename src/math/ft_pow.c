#include <ft/math.h>

int64_t	ft_pow(int64_t n, uint64_t pow)
{
	uint64_t	i;

	if (!pow)
		return (1);
	if (!n)
		return (0);
	i = n;
	pow--;
	while (pow--)
		n *= i;
	return (n);
}
