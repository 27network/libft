#include <stdint.h>
#include <limits.h>

int64_t	ft_abs(int64_t a)
{
	int64_t	mask;

	mask = (a >> (sizeof(int64_t) * CHAR_BIT - 1));
	return ((a + mask) ^ mask);
}
