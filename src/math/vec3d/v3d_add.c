#include <ft/math/vector.h>

__attribute__((always_inline))
t_vec3d	v3d_add(const t_vec3d *v1, const t_vec3d *v2)
{
	return ((t_vec3d){.v = (*(t_v3d *)v1) + (*(t_v3d *)v2)});
}
